package in.sms.command;

/**
 *
 * @author windows 8
 */
public class FacultyCommand {
    private String faculty_id;
    private String first_name;
    private String last_name;
    private String contact_number;
    private String designation;
    private String research_area;
    private String email_primary;
    private String email_secondary;
    private String land_line_number;
    private String title;

    public FacultyCommand() {
    }

    public String getFaculty_id() {
        return faculty_id;
    }

    public void setFaculty_id(String faculty_id) {
        this.faculty_id = faculty_id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getContact_number() {
        return contact_number;
    }

    public void setContact_number(String contact_number) {
        this.contact_number = contact_number;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public String getResearch_area() {
        return research_area;
    }

    public void setResearch_area(String research_area) {
        this.research_area = research_area;
    }

    public String getEmail_primary() {
        return email_primary;
    }

    public void setEmail_primary(String email_primary) {
        this.email_primary = email_primary;
    }

    public String getEmail_secondary() {
        return email_secondary;
    }

    public void setEmail_secondary(String email_secondary) {
        this.email_secondary = email_secondary;
    }

    public String getLand_line_number() {
        return land_line_number;
    }

    public void setLand_line_number(String land_line_number) {
        this.land_line_number = land_line_number;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    
}
